import mongoose from "mongoose";

const orderSchema = new mongoose.Schema(
	{
		user: {
			type: mongoose.Schema.Types.ObjectId,
			ref: "User",
			required: true,
		},
		drinks: [
			{
				drink: {
					type: mongoose.Schema.Types.ObjectId,
					ref: "Drink",
					required: true,
				},
				quantity: {
					type: Number,
					required: true,
					min: 1,
				},
				price: {
					type: Number,
					required: true,
					min: 0,
				},
			},
		],
		totalAmount: {
			type: Number,
			required: true,
			min: 0,
		},
	}, {
        timestamps: true
    }
);

const Order = mongoose.model("Order", orderSchema);

export default Order;